# frozen_string_literal: true

module Ovh::Api
  module Api
    class Support::Tickets
      def initialize(connection)
        @connection = connection
      end

      def create_post(support_tickets_create_post_request:)
        raise ArgumentError, 'support_tickets_create_post_request is required' if support_tickets_create_post_request.nil?

        @connection.call(
          :POST,
          '/support/tickets/create',
          type: nil,
          auth: [],
          body: support_tickets_create_post_request
        )
      end

      def get(archived: nil, category: nil, customer_reply_needed: nil, exclude_autogenerated: nil, max_creation_date: nil, min_creation_date: nil, page: nil, page_size: nil, product: nil, service_name: nil, status: nil, subject: nil, ticket_number: nil)
        @connection.call(
          :GET,
          '/support/tickets',
          type: nil,
          auth: [],
          query: { 'archived' => archived, 'category' => category, 'customerReplyNeeded' => customer_reply_needed, 'excludeAutogenerated' => exclude_autogenerated, 'maxCreationDate' => max_creation_date, 'minCreationDate' => min_creation_date, 'page' => page, 'pageSize' => page_size, 'product' => product, 'serviceName' => service_name, 'status' => status, 'subject' => subject, 'ticketNumber' => ticket_number }
        )
      end

      def ticket_id_can_be_scored_get(ticket_id:)
        raise ArgumentError, 'ticket_id is required' if ticket_id.nil?

        @connection.call(
          :GET,
          '/support/tickets/{ticketId}/canBeScored'
            .gsub('{ticketId}', ERB::Util.url_encode(ticket_id.to_s)),
          type: nil,
          auth: []
        )
      end

      def ticket_id_close_post(ticket_id:)
        raise ArgumentError, 'ticket_id is required' if ticket_id.nil?

        @connection.call(
          :POST,
          '/support/tickets/{ticketId}/close'
            .gsub('{ticketId}', ERB::Util.url_encode(ticket_id.to_s)),
          type: nil,
          auth: []
        )
      end

      def ticket_id_get(ticket_id:)
        raise ArgumentError, 'ticket_id is required' if ticket_id.nil?

        @connection.call(
          :GET,
          '/support/tickets/{ticketId}'
            .gsub('{ticketId}', ERB::Util.url_encode(ticket_id.to_s)),
          type: nil,
          auth: []
        )
      end

      def ticket_id_messages_get(ticket_id:)
        raise ArgumentError, 'ticket_id is required' if ticket_id.nil?

        @connection.call(
          :GET,
          '/support/tickets/{ticketId}/messages'
            .gsub('{ticketId}', ERB::Util.url_encode(ticket_id.to_s)),
          type: nil,
          auth: []
        )
      end

      def ticket_id_reopen_post(ticket_id:, body:)
        raise ArgumentError, 'ticket_id is required' if ticket_id.nil?
        raise ArgumentError, 'body is required' if body.nil?

        @connection.call(
          :POST,
          '/support/tickets/{ticketId}/reopen'
            .gsub('{ticketId}', ERB::Util.url_encode(ticket_id.to_s)),
          type: nil,
          auth: [],
          body: body
        )
      end

      def ticket_id_reply_post(ticket_id:, body:)
        raise ArgumentError, 'ticket_id is required' if ticket_id.nil?
        raise ArgumentError, 'body is required' if body.nil?

        @connection.call(
          :POST,
          '/support/tickets/{ticketId}/reply'
            .gsub('{ticketId}', ERB::Util.url_encode(ticket_id.to_s)),
          type: nil,
          auth: [],
          body: body
        )
      end

      def ticket_id_score_post(ticket_id:, support_tickets_ticket_id_score_post_request:)
        raise ArgumentError, 'ticket_id is required' if ticket_id.nil?
        raise ArgumentError, 'support_tickets_ticket_id_score_post_request is required' if support_tickets_ticket_id_score_post_request.nil?

        @connection.call(
          :POST,
          '/support/tickets/{ticketId}/score'
            .gsub('{ticketId}', ERB::Util.url_encode(ticket_id.to_s)),
          type: nil,
          auth: [],
          body: support_tickets_ticket_id_score_post_request
        )
      end
    end
  end
end
